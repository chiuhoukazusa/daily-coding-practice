/**
 * Marching Cubes 等值面提取
 * 
 * 从3D标量场中提取等值面，渲染成2D图像
 * 使用正交投影 + 光线步进从多角度渲染等值面
 * 
 * 算法流程：
 * 1. 构建3D标量场（使用多个隐式球体的并集）
 * 2. Marching Cubes：对每个体素，计算8个角的符号，查找三角形表
 * 3. 生成三角形网格（顶点位置 + 法线）
 * 4. 光栅化渲染：将三角形投影到2D，Phong着色
 * 
 * 编译: g++ main.cpp -o marching_cubes -std=c++17 -O2
 * 运行: ./marching_cubes
 * 输出: marching_cubes_output.png
 */

