struct Triangle {
    Vec3 v[3];
    Vec3 n[3]; // 每顶点法线
};

// 在边上线性插值，找等值点（isoValue=0）
Vec3 interpolateEdge(Vec3 p1, Vec3 p2, float v1, float v2) {
    if (fabsf(v1 - v2) < 1e-8f) return (p1 + p2) * 0.5f;
    float t = -v1 / (v2 - v1);
    t = std::max(0.0f, std::min(1.0f, t));
    return p1 + (p2 - p1) * t;
}

std::vector<Triangle> marchingCubes(
    int nx, int ny, int nz,
    float xMin, float xMax,
    float yMin, float yMax,
    float zMin, float zMax)
{
    float dx = (xMax - xMin) / nx;
    float dy = (yMax - yMin) / ny;
    float dz = (zMax - zMin) / nz;

    std::vector<Triangle> triangles;

    for (int iz = 0; iz < nz; iz++) {
        for (int iy = 0; iy < ny; iy++) {
            for (int ix = 0; ix < nx; ix++) {

                // 8个角的世界坐标
                Vec3 corners[8];
                float values[8];
                for (int k = 0; k < 8; k++) {
                    corners[k] = {
                        xMin + (ix + cornerOffset[k][0]) * dx,
                        yMin + (iy + cornerOffset[k][1]) * dy,
                        zMin + (iz + cornerOffset[k][2]) * dz
                    };
                    values[k] = scalarField(corners[k]);
                }

                // 计算cubeIndex（哪些角在等值面内）
                int cubeIndex = 0;
                for (int k = 0; k < 8; k++) {
                    if (values[k] < 0.0f) cubeIndex |= (1 << k);
                }

                if (cubeIndex == 0 || cubeIndex == 255) continue;

                // 12条边上的插值点
                Vec3 edgePoints[12];
                for (int e = 0; e < 12; e++) {
                    int a = edgeVertices[e][0];
                    int b = edgeVertices[e][1];
                    edgePoints[e] = interpolateEdge(corners[a], corners[b], values[a], values[b]);
                }

                // 查三角形表
                const int* tris = triTable[cubeIndex];
                for (int t = 0; tris[t] != -1; t += 3) {
                    Triangle tri;
                    for (int v = 0; v < 3; v++) {
                        tri.v[v] = edgePoints[tris[t + v]];
                        tri.n[v] = computeNormal(tri.v[v]);
                    }
                    triangles.push_back(tri);
                }
            }
        }
    }

    printf("  Marching Cubes: 生成 %zu 个三角形\n", triangles.size());
    return triangles;
}

