// 多个球的软并集（smooth union）
float sdSphere(Vec3 p, Vec3 c, float r) {
    return (p - c).length() - r;
}

float smoothMin(float a, float b, float k) {
    float h = std::max(k - fabsf(a - b), 0.0f) / k;
    return std::min(a, b) - h * h * k * 0.25f;
}

// 主标量场：多个球的融合
float scalarField(Vec3 p) {
    // 中心大球
    float d = sdSphere(p, {0.0f, 0.0f, 0.0f}, 1.2f);
    // 四个卫星球
    d = smoothMin(d, sdSphere(p, { 1.4f,  0.5f,  0.0f}, 0.7f), 0.5f);
    d = smoothMin(d, sdSphere(p, {-1.4f,  0.5f,  0.0f}, 0.7f), 0.5f);
    d = smoothMin(d, sdSphere(p, { 0.0f, -1.4f,  0.5f}, 0.6f), 0.5f);
    d = smoothMin(d, sdSphere(p, { 0.0f,  0.5f, -1.4f}, 0.65f), 0.5f);
    return d;
}

// 数值法线（梯度）
Vec3 computeNormal(Vec3 p) {
    const float eps = 0.001f;
    float dx = scalarField({p.x+eps, p.y, p.z}) - scalarField({p.x-eps, p.y, p.z});
    float dy = scalarField({p.x, p.y+eps, p.z}) - scalarField({p.x, p.y-eps, p.z});
    float dz = scalarField({p.x, p.y, p.z+eps}) - scalarField({p.x, p.y, p.z-eps});
    return Vec3(dx, dy, dz).normalized();
}

