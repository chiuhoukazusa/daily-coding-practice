static unsigned int crc32_table[256];
static bool crc32_initialized = false;
static void init_crc32() {
    if (crc32_initialized) return;
    for (int i = 0; i < 256; i++) {
        unsigned int c = (unsigned int)i;
        for (int k = 0; k < 8; k++)
            c = (c & 1) ? (0xEDB88320U ^ (c >> 1)) : (c >> 1);
        crc32_table[i] = c;
    }
    crc32_initialized = true;
}
static unsigned int crc32(const unsigned char* data, size_t len, unsigned int crc = 0xFFFFFFFFU) {
    init_crc32();
    for (size_t i = 0; i < len; i++)
        crc = crc32_table[(crc ^ data[i]) & 0xFF] ^ (crc >> 8);
    return crc ^ 0xFFFFFFFFU;
}

static void write_u32_be(FILE* f, unsigned int v) {
    unsigned char buf[4] = {(unsigned char)(v>>24),(unsigned char)(v>>16),(unsigned char)(v>>8),(unsigned char)v};
    fwrite(buf, 1, 4, f);
}

// 简单的 zlib deflate（使用 fwrite level 0 存储块）
static std::vector<unsigned char> zlib_compress(const unsigned char* data, size_t len) {
    std::vector<unsigned char> out;
    // zlib header: CMF=0x78, FLG=0x01 (deflate, no dict, check bits)
    out.push_back(0x78);
    out.push_back(0x01);

    // DEFLATE non-compressed blocks (BTYPE=00)
    const size_t BLOCK_SIZE = 65535;
    size_t pos = 0;
    while (pos < len) {
        size_t block_len = std::min((size_t)BLOCK_SIZE, len - pos);
        bool is_last = (pos + block_len >= len);
        out.push_back(is_last ? 0x01 : 0x00); // BFINAL | BTYPE=00
        // LEN and NLEN (little-endian)
        out.push_back((unsigned char)(block_len & 0xFF));
        out.push_back((unsigned char)(block_len >> 8));
        out.push_back((unsigned char)(~block_len & 0xFF));
        out.push_back((unsigned char)(~block_len >> 8));
        for (size_t i = 0; i < block_len; i++)
            out.push_back(data[pos + i]);
        pos += block_len;
    }

    // Adler-32 checksum
    unsigned int s1 = 1, s2 = 0;
    for (size_t i = 0; i < len; i++) {
        s1 = (s1 + data[i]) % 65521;
        s2 = (s2 + s1) % 65521;
    }
    unsigned int adler = (s2 << 16) | s1;
    out.push_back((unsigned char)(adler >> 24));
    out.push_back((unsigned char)(adler >> 16));
    out.push_back((unsigned char)(adler >> 8));
    out.push_back((unsigned char)(adler));
    return out;
}

bool Image::savePNG(const char* filename) {
    FILE* f = fopen(filename, "wb");
    if (!f) return false;

    // PNG signature
    const unsigned char sig[8] = {137,80,78,71,13,10,26,10};
    fwrite(sig, 1, 8, f);

    // IHDR chunk
    {
        unsigned char ihdr[13];
        ihdr[0]=(width>>24)&0xFF; ihdr[1]=(width>>16)&0xFF; ihdr[2]=(width>>8)&0xFF; ihdr[3]=width&0xFF;
        ihdr[4]=(height>>24)&0xFF; ihdr[5]=(height>>16)&0xFF; ihdr[6]=(height>>8)&0xFF; ihdr[7]=height&0xFF;
        ihdr[8]=8; // bit depth
        ihdr[9]=2; // color type: RGB
        ihdr[10]=0; ihdr[11]=0; ihdr[12]=0;
        write_u32_be(f, 13);
        fwrite("IHDR", 1, 4, f);
        fwrite(ihdr, 1, 13, f);
        unsigned int c = crc32((const unsigned char*)"IHDR", 4);
        c = crc32(ihdr, 13, c ^ 0xFFFFFFFFU);
        write_u32_be(f, c);
    }

    // Build filtered image data
    std::vector<unsigned char> filtered;
    filtered.reserve((1 + width * 3) * height);
    for (int y = 0; y < height; y++) {
        filtered.push_back(0); // filter type: None
        for (int x = 0; x < width; x++) {
            int idx = (y * width + x) * 3;
            filtered.push_back(data[idx]);
            filtered.push_back(data[idx+1]);
            filtered.push_back(data[idx+2]);
        }
    }

    // IDAT chunk
    {
        auto compressed = zlib_compress(filtered.data(), filtered.size());
        write_u32_be(f, (unsigned int)compressed.size());
        fwrite("IDAT", 1, 4, f);
        fwrite(compressed.data(), 1, compressed.size(), f);
        unsigned int c = crc32((const unsigned char*)"IDAT", 4);
        c = crc32(compressed.data(), compressed.size(), c ^ 0xFFFFFFFFU);
        write_u32_be(f, c);
    }

    // IEND
    write_u32_be(f, 0);
    fwrite("IEND", 1, 4, f);
    write_u32_be(f, crc32((const unsigned char*)"IEND", 4));

    fclose(f);
    return true;
}

