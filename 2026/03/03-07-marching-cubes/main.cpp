struct RenderContext {
    Image& img;
    int W, H;
    std::vector<float> zbuffer;

    // 相机参数
    Vec3 camPos, camRight, camUp, camFwd;
    float orthoWidth, orthoHeight;

    RenderContext(Image& img, Vec3 eye, Vec3 target, Vec3 worldUp, float orthoW)
        : img(img), W(img.width), H(img.height),
          zbuffer(img.width * img.height, 1e30f)
    {
        camFwd = (target - eye).normalized();
        camRight = camFwd.cross(worldUp).normalized();
        camUp = camRight.cross(camFwd).normalized();
        camPos = eye;
        orthoWidth = orthoW;
        orthoHeight = orthoW * H / W;
    }

    // 世界坐标 → 屏幕坐标 + 深度
    bool project(Vec3 p, float& sx, float& sy, float& depth) {
        Vec3 rel = p - camPos;
        float x = rel.dot(camRight);
        float y = rel.dot(camUp);
        float z = rel.dot(camFwd);
        // 正交投影
        sx = (x / orthoWidth + 0.5f) * W;
        sy = (0.5f - y / orthoHeight) * H;
        depth = z;
        return true;
    }

    // Phong 着色
    Vec3 shade(Vec3 pos, Vec3 normal, Vec3 baseColor) {
        Vec3 lightPos1 = {3.0f, 5.0f, 4.0f};
        Vec3 lightPos2 = {-4.0f, 2.0f, -2.0f};
        Vec3 ambient = {0.08f, 0.08f, 0.12f};

        auto computeLight = [&](Vec3 lp, Vec3 lc, float intensity) -> Vec3 {
            Vec3 L = (lp - pos).normalized();
            Vec3 V = (camPos - pos).normalized();
            Vec3 H = (L + V).normalized();
            float diff = std::max(0.0f, normal.dot(L));
            float spec = powf(std::max(0.0f, normal.dot(H)), 64.0f);
            Vec3 result;
            result.x = (baseColor.x * diff + spec * 0.6f) * lc.x * intensity;
            result.y = (baseColor.y * diff + spec * 0.6f) * lc.y * intensity;
            result.z = (baseColor.z * diff + spec * 0.6f) * lc.z * intensity;
            return result;
        };

        Vec3 light1 = computeLight(lightPos1, {1.0f, 0.95f, 0.9f}, 1.0f);
        Vec3 light2 = computeLight(lightPos2, {0.5f, 0.6f, 0.8f}, 0.4f);

        return {
            std::min(1.0f, ambient.x + light1.x + light2.x),
            std::min(1.0f, ambient.y + light1.y + light2.y),
            std::min(1.0f, ambient.z + light1.z + light2.z)
        };
    }

    // 光栅化三角形
    void rasterize(const Triangle& tri, Vec3 color) {
        float sx[3], sy[3], depth[3];
        for (int i = 0; i < 3; i++) project(tri.v[i], sx[i], sy[i], depth[i]);

        // 边界框裁剪
        int minX = std::max(0, (int)std::floor(std::min({sx[0],sx[1],sx[2]}) - 0.5f));
        int maxX = std::min(W-1, (int)std::ceil(std::max({sx[0],sx[1],sx[2]}) + 0.5f));
        int minY = std::max(0, (int)std::floor(std::min({sy[0],sy[1],sy[2]}) - 0.5f));
        int maxY = std::min(H-1, (int)std::ceil(std::max({sy[0],sy[1],sy[2]}) + 0.5f));

        // 三角形面积（2D）
        float area = (sx[1]-sx[0])*(sy[2]-sy[0]) - (sx[2]-sx[0])*(sy[1]-sy[0]);
        if (fabsf(area) < 1e-6f) return;

        for (int y = minY; y <= maxY; y++) {
            for (int x = minX; x <= maxX; x++) {
                float px = x + 0.5f, py = y + 0.5f;
                // 重心坐标
                float w0 = ((sx[1]-sx[2])*(py-sy[2]) + (sy[2]-sy[1])*(px-sx[2])) / area;
                float w1 = ((sx[2]-sx[0])*(py-sy[0]) + (sy[0]-sy[2])*(px-sx[0])) / area;
                float w2 = 1.0f - w0 - w1;
                if (w0 < -0.001f || w1 < -0.001f || w2 < -0.001f) continue;

                float z = w0*depth[0] + w1*depth[1] + w2*depth[2];
                int pidx = y * W + x;
                if (z >= zbuffer[pidx]) continue;
                zbuffer[pidx] = z;

                // 插值法线
                Vec3 N;
                N.x = w0*tri.n[0].x + w1*tri.n[1].x + w2*tri.n[2].x;
                N.y = w0*tri.n[0].y + w1*tri.n[1].y + w2*tri.n[2].y;
                N.z = w0*tri.n[0].z + w1*tri.n[1].z + w2*tri.n[2].z;
                N = N.normalized();

                // 插值世界坐标
                Vec3 pos;
                pos.x = w0*tri.v[0].x + w1*tri.v[1].x + w2*tri.v[2].x;
                pos.y = w0*tri.v[0].y + w1*tri.v[1].y + w2*tri.v[2].y;
                pos.z = w0*tri.v[0].z + w1*tri.v[1].z + w2*tri.v[2].z;

                Vec3 col = shade(pos, N, color);
                img.setPixel(x, y,
                    (unsigned char)(std::min(1.0f, col.x) * 255),
                    (unsigned char)(std::min(1.0f, col.y) * 255),
                    (unsigned char)(std::min(1.0f, col.z) * 255));
            }
        }
    }
};

