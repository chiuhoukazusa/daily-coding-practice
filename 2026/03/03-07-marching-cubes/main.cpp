void fillBackground(Image& img) {
    for (int y = 0; y < img.height; y++) {
        float t = (float)y / img.height;
        unsigned char r = (unsigned char)(15 + t * 30);
        unsigned char g = (unsigned char)(20 + t * 25);
        unsigned char b = (unsigned char)(35 + t * 50);
        for (int x = 0; x < img.width; x++) {
            img.setPixel(x, y, r, g, b);
        }
    }
}

// ============================================================
// 绘制文字标签（像素字体，简单版）
// ============================================================

void drawText(Image& img, int x, int y, const char* text, unsigned char r, unsigned char g, unsigned char b) {
    // 5x7 像素字体（只包含字母数字和空格）
    static const unsigned char font[][5] = {
        {0x1F,0x11,0x1F,0x11,0x11}, // A
        {0x1E,0x11,0x1E,0x11,0x1E}, // B
        {0x0F,0x10,0x10,0x10,0x0F}, // C
        {0x1E,0x11,0x11,0x11,0x1E}, // D
        {0x1F,0x10,0x1E,0x10,0x1F}, // E
        {0x1F,0x10,0x1E,0x10,0x10}, // F
        {0x0F,0x10,0x17,0x11,0x0F}, // G
        {0x11,0x11,0x1F,0x11,0x11}, // H
        {0x0E,0x04,0x04,0x04,0x0E}, // I
        {0x01,0x01,0x01,0x11,0x0E}, // J
        {0x11,0x12,0x1C,0x12,0x11}, // K
        {0x10,0x10,0x10,0x10,0x1F}, // L
        {0x11,0x1B,0x15,0x11,0x11}, // M
        {0x11,0x19,0x15,0x13,0x11}, // N
        {0x0E,0x11,0x11,0x11,0x0E}, // O
        {0x1E,0x11,0x1E,0x10,0x10}, // P
        {0x0E,0x11,0x15,0x12,0x0D}, // Q
        {0x1E,0x11,0x1E,0x12,0x11}, // R
        {0x0F,0x10,0x0E,0x01,0x1E}, // S
        {0x1F,0x04,0x04,0x04,0x04}, // T
        {0x11,0x11,0x11,0x11,0x0E}, // U
        {0x11,0x11,0x11,0x0A,0x04}, // V
        {0x11,0x11,0x15,0x1B,0x11}, // W
        {0x11,0x0A,0x04,0x0A,0x11}, // X
        {0x11,0x0A,0x04,0x04,0x04}, // Y
        {0x1F,0x02,0x04,0x08,0x1F}, // Z
        {0x00,0x00,0x00,0x00,0x00}, // space (26)
        {0x0E,0x13,0x15,0x19,0x0E}, // 0 (27)
        {0x04,0x0C,0x04,0x04,0x0E}, // 1
        {0x0E,0x01,0x06,0x08,0x0F}, // 2
        {0x0E,0x01,0x06,0x01,0x0E}, // 3
        {0x02,0x06,0x0A,0x1F,0x02}, // 4
        {0x1F,0x10,0x1E,0x01,0x1E}, // 5
        {0x0E,0x10,0x1E,0x11,0x0E}, // 6
        {0x1F,0x01,0x02,0x04,0x04}, // 7
        {0x0E,0x11,0x0E,0x11,0x0E}, // 8
        {0x0E,0x11,0x0F,0x01,0x0E}, // 9 (36)
        {0x00,0x04,0x00,0x04,0x00}, // : (37)
        {0x00,0x00,0x1F,0x00,0x00}, // - (38)
    };

    int cx = x;
    for (const char* c = text; *c; c++) {
        int idx = -1;
        if (*c >= 'A' && *c <= 'Z') idx = *c - 'A';
        else if (*c == ' ') idx = 26;
        else if (*c >= '0' && *c <= '9') idx = 27 + (*c - '0');
        else if (*c == ':') idx = 37;
        else if (*c == '-') idx = 38;
        else if (*c >= 'a' && *c <= 'z') idx = *c - 'a'; // 小写映射大写

        if (idx >= 0 && idx < (int)(sizeof(font)/sizeof(font[0]))) {
            for (int row = 0; row < 5; row++) {
                for (int col = 0; col < 5; col++) {
                    if (font[idx][row] & (0x10 >> col)) {
                        img.setPixel(cx + col, y + row, r, g, b);
                    }
                }
            }
        }
        cx += 6;
    }
}

// ============================================================
// 主程序
// ============================================================

int main() {
    printf("=== Marching Cubes 等值面提取 ===\n\n");

    const int GRID = 64;       // 体素网格分辨率
    const float RANGE = 2.5f;  // 场景范围
    const int W = 800, H = 600;

    printf("[1/4] 执行 Marching Cubes...\n");
    printf("  网格分辨率: %d x %d x %d\n", GRID, GRID, GRID);
    printf("  场景范围: [%.1f, %.1f]\n", -RANGE, RANGE);

    auto triangles = marchingCubes(
        GRID, GRID, GRID,
        -RANGE, RANGE, -RANGE, RANGE, -RANGE, RANGE
    );

    if (triangles.empty()) {
        fprintf(stderr, "错误：没有生成任何三角形！\n");
        return 1;
    }

    printf("\n[2/4] 渲染图像 (%d x %d)...\n", W, H);

    // 主视图（稍微倾斜的正交投影）
    Image img(W, H);
    fillBackground(img);

    Vec3 eye   = {4.0f, 3.5f, 5.0f};
    Vec3 target = {0.0f, 0.0f, 0.0f};
    Vec3 worldUp = {0.0f, 1.0f, 0.0f};

    RenderContext rc(img, eye, target, worldUp, 5.5f);

    // 物体颜色：渐变青绿色
    Vec3 meshColor = {0.4f, 0.75f, 0.65f};

    int count = 0;
    for (const auto& tri : triangles) {
        rc.rasterize(tri, meshColor);
        count++;
        if (count % 10000 == 0) {
            printf("  进度: %d/%zu 三角形\r", count, triangles.size());
            fflush(stdout);
        }
    }
    printf("  渲染完成: %zu 三角形                    \n", triangles.size());

    // 添加文字标签
    drawText(img, 10, 10, "MARCHING CUBES", 180, 220, 200);
    drawText(img, 10, 20, "SMOOTH UNION SDF", 160, 190, 170);

    printf("\n[3/4] 保存主视图 marching_cubes_output.png...\n");
    if (!img.savePNG("marching_cubes_output.png")) {
        fprintf(stderr, "错误：无法保存 PNG 文件！\n");
        return 1;
    }
    printf("  ✅ 已保存 marching_cubes_output.png\n");

    // 侧视图
    printf("\n[4/4] 渲染侧视图 marching_cubes_side.png...\n");
    Image img2(W, H);
    fillBackground(img2);

    Vec3 eye2   = {5.5f, 1.0f, 0.5f};
    RenderContext rc2(img2, eye2, target, worldUp, 5.5f);
    for (const auto& tri : triangles) {
        rc2.rasterize(tri, {0.7f, 0.55f, 0.45f}); // 暖棕色
    }
    drawText(img2, 10, 10, "SIDE VIEW", 200, 180, 170);

    if (!img2.savePNG("marching_cubes_side.png")) {
        fprintf(stderr, "错误：无法保存侧视图！\n");
        return 1;
    }
    printf("  ✅ 已保存 marching_cubes_side.png\n");

    printf("\n=== 完成 ===\n");
    printf("输出文件:\n");
    printf("  - marching_cubes_output.png (主视图)\n");
    printf("  - marching_cubes_side.png   (侧视图)\n");
    printf("三角形数量: %zu\n", triangles.size());
    return 0;
}
